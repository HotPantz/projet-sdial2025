CREATE TABLE groupe (
    id INT AUTO_INCREMENT PRIMARY KEY,
    identifiant VARCHAR(50) NULL,
    id_sujet INT,
    id_ue INT,
    id_eleve INT,
    FOREIGN KEY (id_sujet) REFERENCES sujet(id),
    FOREIGN KEY (id_ue) REFERENCES unite_enseignement(id),
    FOREIGN KEY (id_eleve) REFERENCES personne(id)
);

CREATE TABLE session (
    id INT AUTO_INCREMENT PRIMARY KEY,
    identifiant VARCHAR(50) NULL,
    id_ue INT,
    id_classe INT,
    id_creneau INT,
    FOREIGN KEY (id_ue) REFERENCES unite_enseignement(id),
    FOREIGN KEY (id_classe) REFERENCES classe(id),
    FOREIGN KEY (id_creneau) REFERENCES creneau(id)
);

CREATE TABLE reservation (
    id INT AUTO_INCREMENT PRIMARY KEY,
    identifiant VARCHAR(50) NULL,
    id_personne INT,
    id_salle INT,
    id_creneau INT,
    FOREIGN KEY (id_personne) REFERENCES personne(id),
    FOREIGN KEY (id_salle) REFERENCES salle(id),
    FOREIGN KEY (id_creneau) REFERENCES creneau(id)
);
